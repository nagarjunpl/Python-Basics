
# String Methods

course = "  Python For Beginners  "

print(course.upper()) # convert into upper case

print(course.lower()) # convert into lower case

print(course.replace("for beginners", "for experts")) # replace the word

print(course.find("For")) # find the index of the word

print(course.strip()) # remove the white spaces from the beginning and end of the string

print(course.center(100)) # center the string

print(len(course)) # length of the string

